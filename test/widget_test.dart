import 'package:flutter/material.dart';
import 'package:flutter_test/flutter_test.dart';

import 'package:hidden_camera_detector/src/presentation/app/app.dart';

void main() {
  testWidgets('Onboarding headline is shown when not completed', (tester) async {
    tester.view.physicalSize = const Size(1080, 1920);
    tester.view.devicePixelRatio = 1.0;
    addTearDown(() {
      tester.view.resetPhysicalSize();
      tester.view.resetDevicePixelRatio();
    });

    await tester.pumpWidget(const HiddenCameraDetectorApp(onboardingCompleted: false));

    await tester.pump();

    expect(find.text('Protect your privacy'), findsOneWidget);
  });
}
